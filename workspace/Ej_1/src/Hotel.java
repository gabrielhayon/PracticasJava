
public class Hotel {
	
    private int numRooms;
    private double roomPrice;
    public int getNumRooms() {
        return this.numRooms;
    }
    public void setNumRooms(int cantHabitaciones) {
        this.numRooms = cantHabitaciones;
    }
    public double getRoomPrice() {
        return this.roomPrice;
    }
    public void setRoomPrice(double precioHabitaciones) {
        this.roomPrice = precioHabitaciones;
    }
    
}
